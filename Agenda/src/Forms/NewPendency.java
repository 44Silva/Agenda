    private void rb_TarefaStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_rb_TarefaStateChanged
        
        if (rb_Tarefa.isSelected()){
            tf_DataLimite.setEnabled(true);
        }
        else {
            tf_DataLimite.setEnabled(false);
            tf_DataLimite.setText("");
        }
        
        
    }//GEN-LAST:event_rb_TarefaStateChanged

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        
        tf_DataLimite.setText(currentDateTime);
        
        try {

            String[] arrTipos = arrTipos = arquivos.getConfigTipos();

            for (int i = 0; i < arrTipos.length; i++) {
                cb_Tipo.addItem(arrTipos[i]);
            }

        } catch (IOException ex) {
            Logger.getLogger(NewPendency.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_formWindowOpened

