    private void check_tipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_check_tipoActionPerformed
        if (!check_tipo.isSelected()){
            tf_NovoTipo.setEnabled(true);
            cb_Tipo.setEnabled(false);
        }
        else {
            tf_NovoTipo.setText("");
            tf_NovoTipo.setEnabled(false);
            cb_Tipo.setEnabled(true);
        }
    }//GEN-LAST:event_check_tipoActionPerformed

